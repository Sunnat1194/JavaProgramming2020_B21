package Day19_ForLoop;

public class ForLoop {
    public static void main(String[] args) {

        for(int i = 1; i <= 25; i++ ){
            System.out.println("hello");

            System.out.println("=============================================================");

            String name = "Sunnat";
            for (int s=5; s > 0 ; s--) {
                System.out.println(name);
            }

            System.out.println("Hello Batch 21");


        }
    }
}
